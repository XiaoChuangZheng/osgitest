package cn.zxc.module.bullet;

public abstract class Bullet {
	/**
	 * 速度
	 */
	public int speed;
	/**
	 * x坐标
	 */
	public int x;
	/**
	 * y坐标
	 */
	public int y;
	/**
	 * 方向
	 */
	public int derection;
	/**
	 * 生命状态
	 */
	public boolean isDead;
	
	public abstract void move(int derection,int x,int y,int speed);
}
